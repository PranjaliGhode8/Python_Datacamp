# Loop to print numbers from 1 to 10
for i in range(1, 11):  # Starts at 1, stops before 11 (prints 1 to 10)
    print(i)  

# Loop to print odd numbers from 1 to 10
for i in range(1, 11, 2):  # Starts at 1, stops before 11, increments by 2 (prints 1, 3, 5, 7, 9)
    print(i)  

# Loop to print numbers from 11 down to 1 (reverse order)
for i in range(11, 0, -1):  # Starts at 11, stops before 0, decrements by 1 (prints 11 to 1)
    print(i)  

